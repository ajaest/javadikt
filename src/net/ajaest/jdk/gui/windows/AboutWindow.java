    private void JDKInfoLabelHyperlinkUpdate(javax.swing.event.HyperlinkEvent evt) {//GEN-FIRST:event_JDKInfoLabelHyperlinkUpdate
        if (HyperlinkEvent.EventType.ACTIVATED.equals(evt.getEventType())){
            if (Desktop.isDesktopSupported()) {
			Desktop desktop = Desktop.getDesktop();
			try {
				desktop.browse(evt.getURL().toURI());
			} catch (Exception e) {}
		} 
        }
    }//GEN-LAST:event_JDKInfoLabelHyperlinkUpdate

